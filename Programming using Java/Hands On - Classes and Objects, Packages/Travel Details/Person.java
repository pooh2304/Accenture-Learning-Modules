public class Person {
    private String name;
    private char gender;
    private int age;
    
    public String getName() {
        return name;
    }
    public char getGender() {
        return gender;
    }
    public int getAge() {
        return age;
    }
    public void setName(String s) {
        name=s;
    }
    public void setGender(char a) {
        gender=a;
    }
    public void setAge(int n) {
        age=n;
    }
}